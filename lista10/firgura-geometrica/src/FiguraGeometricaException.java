
public class FiguraGeometricaException extends RuntimeException {

    public FiguraGeometricaException() {
    }

    public FiguraGeometricaException(String message) {
        super(message);
    }

    public FiguraGeometricaException(Throwable cause) {
        super(cause);
    }

    public FiguraGeometricaException(String message, Throwable cause) {
        super(message, cause);
    }

    public FiguraGeometricaException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}